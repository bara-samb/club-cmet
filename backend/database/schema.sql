-- 11. Cotisations Table (Dues/Contributions)
CREATE TABLE public.cotisations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID REFERENCES public.users(id) ON DELETE SET NULL,
    nom TEXT NOT NULL,
    classe TEXT NOT NULL,
    montant NUMERIC NOT NULL,
    date_paiement TEXT NOT NULL,
    statut TEXT DEFAULT 'valide' NOT NULL, -- 'valide', 'en_attente'
    enregistre_par TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

ALTER TABLE public.cotisations ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Allow users to view their own cotisations" ON public.cotisations FOR SELECT USING (user_id = auth.uid());
CREATE POLICY "Allow users to insert their cotisations" ON public.cotisations FOR INSERT WITH CHECK (user_id = auth.uid());
CREATE POLICY "Allow admin full access to cotisations" ON public.cotisations FOR ALL USING (
    EXISTS (
        SELECT 1 FROM public.users
        WHERE users.id = auth.uid() AND users.role = 'admin'
    )
);
